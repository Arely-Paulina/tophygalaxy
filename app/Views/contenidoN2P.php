                    Cuando una productora de cine consigue los derechos de algún videojuego, la preocupación de los gamers se hace notar. Tal es el caso de la producción fílmica live-action de Uncharted, protagonizada por Tom Holland. Afortunadamente, el popular actor reveló la primera foto de el como Nathan Drake a través de su cuenta de Instagram, y los fans no podrían estar más contentos.
                    <br><br>
                    Además, el actor de voz de Nathan Drake en la franquicia videojueguil de Uncharted, Nolan North, expresó su emoción de tener a Tom Holland interpretando el mismo papel en la versión live-action. 
                    <br><br>
