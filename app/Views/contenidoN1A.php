                    La cuenta oficial de Twitter del bloque de anime Noitama anuncio que la segunda temporada de la serie The Promised Neverland se estrenará el 7 de enero en Fuji TV.
                    <br><br>
                    Junto al anuncio se reveló un nuevo teaser tráiler que nos mantiene al borde de nuestras pantallas, esperando que la segunda temporada esté disponible en el occidente. 
                    <br><br>
                    Originalmente, la segunda temporada iba a estrenarse durante octubre del 2020, pero se vio retrasada debido a la contingencia por Covid-19 y las dificultades de producción que se generaron. La primera temporada de The Promised Neverland está actualmente disponible a través de Crunchyroll y Netflix. Se desconoce cuando llegará la segunda temporada a dichas plataformas.
