                    Cuando la película Kimetsu no Yaiba: Mugen Train estrenó el 16 de octubre en Japón, nadie esperaba el nivel de éxito que sorprendió a los cinemas. El éxito no solo sorprendió a la gente común y corriente, sino al exitoso director Makoto Shinkai también.
                    <br><br>
                    El director de Your Name, la película anime más taquillera del mundo, habló sobre el filme Mugen Train y expresó lo que opina de su inesperado nivel de éxito.
                    <br><br>
                    “¡Guau! … ¿Cómo es posible esto en la taquilla japonesa? ¡Un gran logro! Kimetsu no Yaiba: Mugen Train fue estrenada hace tres días, comenzando las ventas en 4,600 millones de yenes.”
                    <br><br>
                    Demon Slayer: Mugen Train superó al resto de películas anime durante el fin de semana de estreno, vendiendo más que los éxitos Dragon Ball Super: Broly y Wolf Children. Muchos han comparado el estreno de Mugen Train con las producciones de Makoto Shinkai: Weathering With You y Your Name, aún no superadas en taquilla. Habrá que esperar al próximo fin de semana para ver si la película de Kimetsu no Yaiba alcanza o supera los records de Shinkai.
