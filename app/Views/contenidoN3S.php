                <h1 class="huno">Live-action de Assassin’s Creed llegará a Netflix</h1>
                    <p class="fecha">27 de Octubre del 2020, 14:24</p>
                    <span class="badge badge-primary">Noticias</span>
                    <span class="badge badge-primary">Series</span><br>
                    <br>
                    <img src="<?php echo base_url(); ?>/assets/images/ns3.jpeg" class="rounded img-fluid mx-auto d-block" width="650" height="300">        
                    <p>
                    <br>
                    Si hablamos de adaptaciones de videojuegos a series, Netflix es el primero en hacerse presente. Hoy, la plataforma de streaming anuncia otro proyecto más, involucrando a la franquicia Assassin’s Creed.
                    <br><br>
                    La colaboración entre Netflix y Ubisoft llevará a la pantalla una adaptación live-action con el equipo de Filme y Televisión de Jason Altman y Danielle Kreinik. Ambas compañías buscan un showrunner para empezar la revisión del “épico” proyecto.
                    <br><br>
                    El teaser presentado por el Twitter oficial de Netflix nos prepara para lo que podría ser una adaptación inspirada en la trilogía de Ezio Auditore. La música presenciada en el video de diez segundos es nada más y nada menos que “Ezio’s Family”, tema principal de Assassin’s Creed 2.
                    <br><br>
                    La serie live-action de Assassin’s Creed está siendo desarrollada por Netflix y Ubisoft.
