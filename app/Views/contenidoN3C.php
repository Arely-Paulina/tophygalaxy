                    Así de imponente luce The Batman Who Frags, la fusión del Caballero Oscuro con Lobo en el Multiverso Oscuro de DC.
                    <br><br>
                    La nueva serie de cómics de DC Dark Nights: Death Metal nos ha brindado unas fusiones un tanto curiosas de Batman con otros personajes clave de DC. Ahora la última en mostrarse ha sido en un one shot titulado Dark Nights: Death Metal Infinite Hours Exxxtreme, un extenso cómic de 48 páginas en el que se ha mostrado a The Batman Who Fags, la fusión de Batman con Lobo.
                    <br><br>
                    Este nuevo ser del Multiverso Oscuro de DC es el resultado de que Bruce Wayne se haya inyectado ADN de Czarnian para mejorar su fuerza y ​​factor de curación, según detalla la sinopsis del cómic, la cual además promete buenas dosis de violencia gratuita.
                    <br><br>
                    "¡Es la Hora Infinita del Tío Lobo! Es tu oportunidad de dejar que el Hombre Principal te Lobo-tomice con historias familiares pero raras del Universo DC exactamente como las recuerdas: ¡con sangre y tripas y una violencia gratuita extrema!", se puede leer en la descripción del cómic de DC.
                    <br><br>
                    Este nuevo one shot está realizado por Denys Cowan, Tyler Kirkham, Rags Morales, Sam Humphries, Becky Cloonan y Frank Tieri, y su lanzamiento en Estados Unidos está previsto para mañana 10 de noviembre de 2020. Por el momento se desconoce cuándo llegará el cómic a España.
