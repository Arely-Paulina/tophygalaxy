                    Todos los [Marcos] en Houseparty son colecciones originales creadas por talentosos artistas independientes. Cada ciertas semanas, en la app aparecerá una nueva colección realizada por un nuevo artista. Para comenzar, se tendrá el sensacional estilo pop-art de @lebassis. Leandro Assis es un artista y director de arte brasileño que vive en Río. Es conocido por tipografías súper intensas, paletas coloridas e ilustraciones divertidas. Además de eso, le apasiona encontrar oportunidades para usar el diseño como medio para hablar de las cosas que más le importan, como la cultura negra, temas de género y derechos LGBTQIA+.
                    <br><br>
                    Acerca de la colección – “Al pensar en estos primeros 15 [Marcos], me acordé de todas mis referencias de stickers de cuando era un adolescente, con esos mega cuadernos llenos de stickers que amaba coleccionar, pero que ¡nunca me atreví a utilizar! Imaginaba una vibra muy divertida que funcionará para todo tipo de personas. Quiero hacer feliz a las personas que estén en una llamada en estos tiempos difíciles.”
