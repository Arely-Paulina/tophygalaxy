                <h1 class="huno">Resident Evil: Infinite Darkness será canon para la franquicia de videojuegos</h1>
                    <p class="fecha">27 de Octubre del 2020, 15:29</p>
                    <span class="badge badge-primary">Noticias</span>
                    <span class="badge badge-primary">Series</span><br>
                    <br>
                    <img src="<?php echo base_url(); ?>/assets/images/ns2.jpg" class="rounded img-fluid mx-auto d-block" width="650" height="300">        
                    <p>
                    <br>
                    Luego de haber anunciado una nueva serie basada en la aclamada franquicia videojueguil de zombies, Netflix revela los primeros detalles entorno a la historia de Resident Evil: Infinite Darkness. A través de Twitter, el servicio de streaming confirmó que los acontecimientos de la serie serán canon en la historia de los videojuegos.
                    <br><br>
                    Netflix también mostró dos imágenes de los protagonistas de Resident Evil: Infinite Darkness. Anteriormente, ya habíamos visto a Claire Redfield y a Leon S. Kennedy en el teaser tráiler publicado por Netflix, sin embargo, verlos nuevamente en el nuevo post nos deja disfrutar un poco más el seguimiento de la producción.
                    <br><br>
                    <img src="<?php echo base_url(); ?>/assets/images/ns21.jpg" class="rounded img-fluid mx-auto d-block" width="650" height="300">
                    <br><br>
                    Como era de esperar, “más experimentos biológicos escapan, y solo un par de veteranos cazadores de zombies pueden arreglarlo”. Hasta el momento, no se sabe cómo, cuando o donde ocurrirá la trama de Infinite Darkness, solo que la serie CGI llegará a Netflix durante el año 2021.
                    <br><br>
                    Resident Evil: Infinite Darkness se estrenará en Netflix el próximo año.
