                <h1 class="huno">Xbox Game Pass ahora incluye anime gracias a colaboración con Funimation</h1>
                    <p class="fecha">2 de Octubre del 2020, 13:54</p>
                    <span class="badge badge-primary">Noticias</span>
                    <span class="badge badge-primary">Anime</span><br>
                    <br>
                    <img src="<?php echo base_url(); ?>/assets/images/na3.jpg" class="rounded img-fluid mx-auto d-block" width="650" height="300">        
                    <p>
                    <br>
                    A pesar de lo que puedan pensar algunos gamers de las consolas de nueva generación o servicios videojueguiles, no hay duda que Xbox Game Pass Ultimate ofrece un gran servicio por un precio accesible. Aunque juegos gratuitos de varias plataformas Xbox pueda ser suficiente, Microsoft nos tiene otra sorpresa: Anime gratis.
                    <br><br>
                    Microsoft ofrece una membresía gratuita Premium Plus para Funimation de dos meses para todos los suscriptores de Xbox Game Pass Ultimate. El servicio de anime en línea incluye un listado de grandes series como Dragon Ball, Boku no Hero Academia, Cowboy Bebop, Shingeki no Kyojin, Fullmetal Alchemist: Brotherhood y Afro Samurai. Jugadores de Xbox y Windows podrán disfrutar de un catalogo de más de 600 animes en el servicio de transmisión en línea de Funimation.
                    <br><br>
                    <img src="<?php echo base_url(); ?>/assets/images/na31.jpeg" class="rounded img-fluid mx-auto d-block" width="650" height="300"> 
                    <br><br>
                    Ahora con esta nuevo asociación entre ambas compañías, por $14.99 USD, no solo disfrutarás de cientos de videojuegos gratis y con descuentos, sino de cientos de animes sin costo adicional.
                    <br><br>
                    Por el momento, Funimation solo está disponible en Estados Unidos, Canada, Reino Unido, Irlanda, Australia y Nueva Zelanda.
                    </p>
