                    <p>
                    <br>
                    Llevamos muchos años sin una nueva entrega de Silent Hill, pero eso no quiere decir que la gente lo haya olvidado. De hecho, sigue siendo parte de la mente colectiva y como prueba tenemos que se hizo tendencia en Twitter debido a un partido de futbol de la Selección Nacional de México.
                    <br><br>
                    En la tarde de este martes, 17 de noviembre, México disputó un partido amistoso contra la selección de Japón. Se trató de un encuentro regular que terminó 2-0 a favor de nuestra selección gracias a los goles marcados por Hirvng Lozano y Raúl Jiménez.
                    <br><br>
                    Como puedes imaginar, el resultado no fue lo que provocó que Silent Hill se volviera tendencia, entonces, ¿qué fue? Resulta que el partido se llevó a cabo en el Stadion Graz-Liebenau, un estadio de la ciudad Graz en Austria. Ahí la temperatura era baja y había mucha humedad, lo que provocó la aparición de neblina.
                    <br><br>
                    Así pues, en la transmisión que miles de personas sintonizaron para ver el partido era muy difícil presenciar las jugadas. Esto ya que la neblina apenas y dejaba ver qué estaba sucediendo en el campo de juego.
                    <br><br>
                    <h4 class="huno">LA COMUNIDAD COMPARÓ EL ENCUENTRO CON SILENT HILL</h4>
                    <hr class="lineaDiv">
                    Si llevas tiempo en las redes sociales sabes muy bien que, cada vez que aparece mucha neblina, no falta quien lo compara con Silent Hill. En esta ocasión fueron miles de personas quienes lo hicieron.
                    <br><br>
                    Así pues, a minutos de haber terminado el partido, Silent Hill se convirtió en una tendencia de Twitter. Esto gracias a un montón de usuarios que decidieron hacer chistes y comentarios comparando el estadio con el popular juego de Konami.
